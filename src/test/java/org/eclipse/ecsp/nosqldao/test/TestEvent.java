package org.eclipse.ecsp.nosqldao.test;

import com.harman.ignite.entities.AbstractIgniteEvent;
import com.harman.ignite.entities.IgniteEvent;
import dev.morphia.annotations.Entity;
import dev.morphia.annotations.Field;
import dev.morphia.annotations.Id;
import dev.morphia.annotations.Index;
import dev.morphia.annotations.IndexOptions;
import dev.morphia.annotations.Indexes;

import java.util.List;

/**
 * TestEvent class.
 */
@Entity("testEvents")
@Indexes(@Index(fields = { @Field("manufacturer"),
    @Field("model"), @Field("year") }, options = @IndexOptions(unique = true)))
public class TestEvent extends AbstractIgniteEvent {

    private static final long serialVersionUID = -1785142819686025246L;
    public String manufacturer;
    public String model;
    public String year;
    public Double price;
    @Id
    private String id;

    @Override
    public List<IgniteEvent> getNestedEvents() {
        return null;
    }

    public String getId() {
        return id;
    }

    @Id
    public void setId(String id) {
        this.id = id;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

}